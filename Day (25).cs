var tmpSlot = new List<List<bool>>();
var sloten = new List<List<List<bool>>>();
sloten.Add(tmpSlot);
foreach (var line in input.Split(Environment.NewLine))
{
    if (line == "")
    {
        tmpSlot = new List<List<bool>>();
        sloten.Add(tmpSlot);
    }
    else
    {
        tmpSlot.Add(line.ToArray().Select(x => x == '#').ToList());
    }
}




foreach (var slot in sloten)
{
    foreach (var other in sloten.SkipWhile(x => x != slot).Skip(1))
    {
        for (int i = 0; i < other.Count; i++)
        {
            for (int j = 0; j < other[0].Count; j++)
            {
                if (slot[i][j] && other[i][j])
                {
                    goto nextOther;
                }
            }
        }
        result++;
    nextOther:;
    }
}

